#!/usr/bin/env python3
"""
Module with type-annotated function add.
"""


def add(a: float, b: float) -> float:
    """
    Function that takes two float arguments and returns their sum as a float.

    Parameters:
        a (float): The first input number.
        b (float): The second input number.

    Returns:
        float: The sum of the input numbers.
    """
    return a + b
